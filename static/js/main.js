    init () {
      this.cacheElements();
      if (this.highlightElement !== null && this.homeArtElement !== null && this.homeStudioElement !== null) {
        this.getHighlightedArt();
        this.getRelatedItems(0, database.art, this.homeArtElement);
        this.getRelatedItems(0, database.studio, this.homeStudioElement);
      }

      if (this.artElement !== null && this.filtersCategoryElement !== null && this.filtersYearsElement !== null) {
        this.generateUIForFilters(database.filtersCategory, this.filtersCategoryElement);
        this.generateUIForFilters(database.filtersYear, this.filtersYearsElement);
        this.generateUIForArt();
      }

      if (this.studioElement !== null) {
        this.generateUIForStudio();
      }

      if (this.releaseElement !== null && this.inThePressElement !== null) {
        this.generateUIForPress();
      }

      if (this.detailPressElement !== null) {
        this.generateUIForDetailPress(2);
      }

      if (this.detailArtElement !== null && this.relatedElement !== null) {
        this.generateUIForDetailArtStudio(1, database.art, this.detailArtElement);
        this.getRelatedItems(1, database.art, this.relatedElement);
      }

      if (this.detailStudioElement !== null && this.relatedElement !== null) {
        console.log(this.detailStudioElement)
        this.generateUIForDetailArtStudio(2, database.studio, this.detailStudioElement);
        this.getRelatedItems(2, database.studio, this.relatedElement);
      }

      if (this.timeElements !== null) {
        this.getCurrentTime();
      }

      if (this.emailFooterElement !== null) {
        this.showSubscribeButton();
      }
    },
    cacheElements() {
      this.highlightElement = document.querySelector('.highlight');
      this.homeArtElement = document.querySelector('.home-art');
      this.homeStudioElement = document.querySelector('.home-studio');
      this.studioElement = document.querySelector('.studio');
      this.detailStudioElement = document.querySelector('.detail-container-studio');
      this.releaseElement = document.querySelector('.release');
      this.inThePressElement = document.querySelector('.in-the-press');
      this.detailPressElement = document.querySelector('.detail-container-press');
      this.artElement = document.querySelector('.art');
      this.detailArtElement = document.querySelector('.detail-container-art');
      this.relatedElement = document.querySelector('.related-items');
      this.filtersCategoryElement = document.querySelector('.filters-category');
      this.filtersYearsElement = document.querySelector('.filters-year');
      this.timeElements = document.querySelectorAll('.time');
      this.emailFooterElement = document.querySelector('.email')

      if (this.filtersCategoryElement !== null && this.filtersYearsElement !== null) {
        this.filtersCategoryElement.addEventListener('click', ev => {
          const dataName = ev.target.dataset.name || ev.target.parentNode.dataset.name || ev.target.parentNode.parentNode.dataset.name;
          const artElements = document.querySelectorAll('.work');
          
          artElements.forEach((pElem) => {
            if (dataName !== 'All') {
              if (pElem.dataset.cat !== dataName) {
                pElem.classList.add('hidden');
              } else {
                pElem.classList.remove('hidden');
              }
            } else {
              pElem.classList.remove('hidden');
            }            
          });
        });
        this.filtersYearsElement.addEventListener('click', ev => {
          const dataName = ev.target.dataset.name || ev.target.parentNode.dataset.name || ev.target.parentNode.parentNode.dataset.name;
          const artElements = document.querySelectorAll('.work');
          
          artElements.forEach((pElem) => {
            if (dataName !== 'All') {
              if (pElem.dataset.year !== dataName) {
                pElem.classList.add('hidden');
              } else {
                pElem.classList.remove('hidden');
              }
            } else {
              pElem.classList.remove('hidden');
            }            
          });
        });
      } 
    },
